/**
#### Operadores Condicionais
 (if) se a condição for verdade então..
 (else if) se não se ... então...
 (else) se não então...
 */

let idade = 25;

if (idade < 25) {
  alert("Menor que 25");
} else if (idade > 25) {
  alert("Maior que 25");
} else {
  alert("25 anos")
}

if (25 == 25) {
  alert("25 anos");
} else if (idade > 25) {
  alert("Maior que 25")
} else {
  alert("Menor que 25");
}