/**
#### Operadores de Comparação
 (>) Maior que 
 (<) Menor que 
 (>=) Maior igual
 (<=) Menor igual
 (==) Valor igual
 (!=) Valor diferente
 (===) Valor e tipo iguais **(Não vamos usar agora)
 (!==) Valor e tipo diferentes **(Não vamos usar agora)
 */

// verdadeiro ou falso
//    (true)      (false)

alert(2 == 2); //true
alert(2 == 3); //false
alert(2 < 3); //true
alert(2 != 5); //true
alert(2 != 2); //false

alert("programacao" == "programacao"); //true
alert("programacao" == "ti"); //false